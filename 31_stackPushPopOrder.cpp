#include <cstdio>
#include <stack>
#include <iostream>
using namespace std;
bool IsPopOrder(const int* pPush,const int* pPop,int nLength){
    if(nLength<=0 || pPush==nullptr || pPop==nullptr)
        return false;
    stack<int>s;
    int i=0;
    int j=0;
    while(j<nLength){
        if(s.empty()){
            s.push(pPush[i]);
            i++;
        }
        else{
            while(i<nLength && s.top()!=pPop[j]){
                s.push(pPush[i]);
                i++;
            }
            while((!s.empty()) && (s.top()==pPop[j])){
                s.pop();
                j++;
            }
            if(i==nLength && !s.empty())
                return false;
        }
    }
    return true;
}
// ====================测试代码====================
void Test(const char* testName, const int* pPush, const int* pPop, int nLength, bool expected)
{
    if(testName != nullptr)
        printf("%s begins: ", testName);

    if(IsPopOrder(pPush, pPop, nLength) == expected)
        printf("Passed.\n");
    else
        printf("failed.\n");
}

